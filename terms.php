        $(document).ready(function () {
            var termsContainer = $('.terms-content');
            var tocContainer = $('.terms-sidebar');
            var tocLinks = $('.terms-sidebar a');

            tocLinks.on('click', function (event) {
                event.preventDefault();

                var target = $(this.getAttribute('href'));

                if (target.length) {
                    termsContainer.animate({
                        scrollTop: termsContainer.scrollTop() + target.position().top - termsContainer.position().top
                    }, 800);
                }
            });

            termsContainer.on('scroll', function () {
                var scrollPosition = termsContainer.scrollTop();

                $('.terms-section').each(function () {
                    var section = $(this);
                    var sectionTop = section.position().top + termsContainer.scrollTop();
                    var sectionBottom = sectionTop + section.outerHeight();

                    if (scrollPosition >= sectionTop - 20 && scrollPosition < sectionBottom) {
                        var id = section.attr('id');

                        // Highlight the current ToC link
                        tocLinks.removeClass('active-toc');
                        var activeLink = $('.terms-sidebar a[href="#' + id + '"]');
                        activeLink.addClass('active-toc');

                        // Auto-scroll the sidebar to keep the active item visible
                        var linkOffset = activeLink.position().top;
                        var sidebarHeight = tocContainer.height();
                        var linkHeight = activeLink.outerHeight();

                        if (linkOffset < 0 || linkOffset + linkHeight > sidebarHeight) {
                            tocContainer.animate({
                                scrollTop: tocContainer.scrollTop() + linkOffset - sidebarHeight / 2 + linkHeight / 2
                            }, 400);
                        }
                    }
                });
            });
        });
    </script>
